# To get more math functions, we will import math
from math import *
num = 10
num2 = -21
# Convert number into string
print(str(num) + " is my age")
# Get absolute value
print(abs(num2))
# add power to a number
print(pow(num,3))
# Get max or min out of given numbers
print(max(3,5,67,34,num2,6))
print(min(1,2,3,4,5,6,num2,num))
# round off
print(round(3.7))
print(round(8.4))
# square root, floor and ceil from math
print(sqrt(25))
# it will cut out the after decimal value by decreasing the number
print(floor(9.8))
# it will cut out the after decimal value by increasing the number
print(ceil(9.8))


